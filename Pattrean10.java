package com.kodnest.pattrean;

public class Pattrean10 {

	public static void main(String[] args) {
		for (int i = 1; i <= 3; i++) {
			for (int j = 1; j <= 3; j++) {
				System.out.print("* ");
			}
			for (int k = 1; k <= 5; k++) {
				System.out.print("$ ");
			}
			System.out.println("");
		}

	}

}
