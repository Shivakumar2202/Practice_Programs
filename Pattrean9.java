package com.kodnest.pattrean;

public class Pattrean9 {

	public static void main(String[] args) {
		for (int i = 1; i <= 4; i++) {
			for (int j = 1; j <= 4; j++) {
				System.out.print("* ");
			}
			System.out.println("");
		}
		for (int l = 1; l <= 4; l++) {
			for (int k = 1; k <= 4; k++) {
				System.out.print("$ ");
			}
			System.out.println("");
		}
	}

}
