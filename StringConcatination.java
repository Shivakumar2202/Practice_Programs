package String_progremms.level1;

public class StringConcatination {

	public static void main(String[] args) {
		String s1="Shiva";
		
		String s2="kumar";
		
		String s3="A S";
		
		String s4=s1+s2+" "+s3;
		System.out.println(s4);
		
		System.out.println("-----------------------------");
		System.out.println(s1.concat(s2.concat(" "+s3)));
		

	}

}
